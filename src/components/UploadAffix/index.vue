<template>
  <div class="upload-container">
    <el-upload
      v-if="!disabled"
      ref="upload"
      :action="baseUrl"
      :data="params['Param']"
      :headers="token"
      :show-file-list="false"
      :on-success="uploadSuccess"
      class="upload-demo"
    >
         <v-btn color="indigo" style="float:left;margin-top:10px 0" dark>
          <v-icon dark>backup</v-icon>
        </v-btn>
    </el-upload>

    <el-table
      v-loading.body="listLoading"
      :default-sort="{prop: 'name', order: 'descending'}"
      :data="filelist"
      element-loading-text="拼命加载中"
      border
      fit
      highlight-current-row
    >
      <el-table-column
        label="文件名"
        prop="filename"
        sortable
        align="center">
        <template slot-scope="scope">{{ scope.row.filename }}</template>
      </el-table-column>
      <el-table-column
        label="上传时间"
        prop="timestamp"
        sortable
        align="center">
        <template slot-scope="scope">
          <span>{{ timestampToTime(scope.row.timestamp) }}</span>
        </template>
      </el-table-column>

      <el-table-column
        label="操作"
        align="center"
        min-width="110px">
        <template slot-scope="scope">
          <v-btn
            color="blue"
            @click="exportfile(scope.row.id)"
          >下载</v-btn>

          <v-btn
      v-if="!disabled"
            color="red"
            @click="delete_file(scope.row.id)"
          >删除</v-btn>
        </template>
      </el-table-column>
    </el-table>
  </div>
</template>
<script>
import { getToken } from '@/utils/auth';
import { GetFileList, deletefile } from '@/api/Public/file';
import { timestampToTime } from '@/utils/index';
import download from '@/utils/download';

export default {
  name: 'UploadAffix',

  props: {
    params: {
      type: Object, //  IsDetail true则   只显示文件list以及download button
      default: () => ({}),
    },
    disabled: {
      type: Boolean,
      default: false,
    },
  },
  data() {
    return {
      filelist: null,
      token: {
        auth: getToken(),
      },
      listLoading: false,
      baseUrl: `${process.env.VUE_APP_BASE_URL}file/upload`,
    };
  },
  watch: {
    'params.Param.MasterID': {
      handler() {
        this.$nextTick(() => {
          this.fetchData_File(this.params.Param);
        });
      },
      immediate: true,
    },
  },
  methods: {
    exportfile(id) {
      download(id);
    },
    delete_file(id) {
      this.$confirm('确认删除?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning',
      }).then(() => {
        deletefile(id).then(() => {
          this.fetchData_File(this.params.Param);
        });
      });
    },
    uploadSuccess() {
      this.fetchData_File(this.params.Param);
      this.$refs.upload.clearFiles();
    },
    fetchData_File(param) {
      this.listLoading = true;

      GetFileList(param).then((response) => {
        this.filelist = response.data.list;
        this.listLoading = false;
      });
    },
    timestampToTime,
  },
};
</script>


